function createMockTokenArt(input: {
  label: string
  baseColor: string
  accentColor: string
}) {
  const { label, baseColor, accentColor } = input
  const svg = `
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 256 256">
      <defs>
        <radialGradient id="fill" cx="50%" cy="30%" r="70%">
          <stop offset="0%" stop-color="${accentColor}" />
          <stop offset="100%" stop-color="${baseColor}" />
        </radialGradient>
      </defs>
      <rect width="256" height="256" rx="48" fill="url(#fill)" />
      <circle cx="128" cy="90" r="42" fill="rgba(255,255,255,0.84)" />
      <path d="M64 206 C78 158 102 138 128 138 C154 138 178 158 192 206" fill="rgba(255,255,255,0.84)" />
      <rect x="28" y="24" width="88" height="30" rx="15" fill="rgba(9,16,21,0.24)" />
      <text x="72" y="45" text-anchor="middle" fill="#f7f3eb" font-family="Arial, sans-serif" font-size="18" font-weight="700">${label}</text>
    </svg>
  `

  return `data:image/svg+xml;charset=UTF-8,${encodeURIComponent(svg)}`
}

function skill(
  id: string,
  nome: string,
  atributoBase: AttributeKey,
  bonusPericia: number,
  resumo: string,
): CharacterSkill {
  return {
    id,
    nome,
    atributoBase,
    bonusPericia,
    resumo,
  }
}

function attack(
  id: string,
  nome: string,
  atributoBase: AttributeKey,
  bonusPericia: number,
  dano: string,
  alcance: string,
  resumo: string,
): CharacterAttack {
  return {
    id,
    nome,
    atributoBase,
    bonusPericia,
    dano,
    alcance,
    resumo,
  }
}

